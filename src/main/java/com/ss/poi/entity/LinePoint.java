package com.ss.poi.entity;
 

public class LinePoint {

    public Double x;
    public Double y;

	public Double getX() {
		return this.x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return this.y;
	}

	public void setY(Double y) {
		this.y = y;
	}
}
